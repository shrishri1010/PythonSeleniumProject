# print 1 to 10 numbers
# By default starting value is zero

for i in range(10):   #By default starting value is zero
    print(i)          # i value is printing over here

print("*****************************************")

# print even numbers between 1 to 10 numbers.
for i in range(2,10,2):   #By default starting value is 2 (initial value, max-range value, increment/decrement)
    print(i)          # i value is printing over here

print("*****************************************")
for i in range(10,1,-1):   #By default starting value is 10 (initial value, max-range value, increment/decrement)
    print(i)          # i value is printing over here

