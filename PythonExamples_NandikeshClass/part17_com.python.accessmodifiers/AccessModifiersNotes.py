'''
Public --- Variables and methods can be shared within and outside the class.
'''

'''
Protected --- The members of a class that are declared protected are only accessible to a class derived from it. 
            #Data members of a class are declared protected by adding a single underscore ‘_’ symbol before the 
            data member of that class. 
'''

'''
Private - The members of a class that are declared private are accessible within the class only, 
          private access modifier is the most secure access modifier. Data members of a class are declared private 
          by adding a double underscore ‘__’ symbol before the data member of that class. 
'''

a=10  #public
_b=20 #protected
__c=30 #private